-- iABS Stream Hub - Supabase SQL (Schema + RLS Policies)
-- Run this in Supabase SQL Editor.
-- Notes:
-- - Uses Auth-based admin access via table: admin_users
-- - RLS enabled on all content tables; only active admins can write.

-- =========================
-- 0) Admin users (RBAC) + Helpers
-- =========================
create extension if not exists "uuid-ossp";

create table if not exists public.admin_users (
  user_id uuid primary key,
  role text not null default 'admin',
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

-- IMPORTANT: functions that reference tables must come after table creation
create or replace function public.is_admin()
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.admin_users au
    where au.user_id = auth.uid()
      and au.is_active = true
  );
$$;

alter table public.admin_users enable row level security;

-- Allow logged-in user to read their own admin row (needed by the app)
drop policy if exists "admin_users_read_self" on public.admin_users;
create policy "admin_users_read_self"
on public.admin_users for select
to authenticated
using (user_id = auth.uid());

-- Admins can manage admin list
drop policy if exists "admin_users_admin_all" on public.admin_users;
create policy "admin_users_admin_all"
on public.admin_users for all
to authenticated
using (public.is_admin())
with check (public.is_admin());

-- Allow authenticated users to insert themselves as admin (for first admin setup)
drop policy if exists "admin_users_insert_self" on public.admin_users;
create policy "admin_users_insert_self"
on public.admin_users for insert
to authenticated
with check (user_id = auth.uid());

