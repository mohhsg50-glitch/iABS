-- =========================
-- 5) Audit triggers (optional but powerful)
-- =========================
create or replace function public.audit_trigger_fn()
returns trigger
language plpgsql
security definer
as $$
declare
  rid text;
begin
  rid := coalesce((to_jsonb(coalesce(new, old))->>'id'), null);
  insert into public.audit_logs(actor_user_id, action, table_name, record_id, summary)
  values (
    auth.uid(),
    tg_op,
    tg_table_name,
    rid,
    case
      when tg_op = 'INSERT' then 'Created record'
      when tg_op = 'UPDATE' then 'Updated record'
      when tg_op = 'DELETE' then 'Deleted record'
      else 'Action'
    end
  );
  return coalesce(new, old);
end;
$$;

-- Attach triggers to key tables
do $$
begin
  -- Clean old triggers if exist
  execute 'drop trigger if exists audit_announcements on public.announcements;';
  execute 'drop trigger if exists audit_sponsors on public.sponsors;';
  execute 'drop trigger if exists audit_highlight_clips on public.highlight_clips;';
  execute 'drop trigger if exists audit_schedule on public.schedule;';
  execute 'drop trigger if exists audit_faqs on public.faqs;';
