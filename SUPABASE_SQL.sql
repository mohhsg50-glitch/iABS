-- Media library
create table if not exists public.media_assets (
  id bigint generated by default as identity primary key,
  title text not null,
  asset_url text not null,
  asset_type text not null default 'image',
  created_at timestamptz not null default now()
);

-- Studio Submissions (public can submit, admin must approve)
create table if not exists public.studio_submissions (
  id bigint generated by default as identity primary key,
  title text not null,
  media_url text not null,
  media_type text not null check (media_type in ('image','video')),
  source text not null default 'direct' check (source in ('direct','tiktok','x','instagram','youtube_shorts')),
  preview_url text,
  width int,
  height int,
  placement text default 'feed' check (placement in ('feed','featured')),
  status text not null default 'pending' check (status in ('pending','approved','rejected')),
  approved_at timestamptz,
  rejected_at timestamptz,
  created_at timestamptz not null default now()
);

-- Likes: one like per device per submission, and max 3 likes per device per day
create table if not exists public.studio_likes (
  id bigint generated by default as identity primary key,
  submission_id bigint not null references public.studio_submissions(id) on delete cascade,
  device_id text not null,
  created_at timestamptz not null default now(),
  unique (submission_id, device_id)
);

create index if not exists studio_likes_device_created_idx on public.studio_likes(device_id, created_at desc);
create index if not exists studio_submissions_status_created_idx on public.studio_submissions(status, created_at desc);

-- Helper: enforce 3 likes/day per device (used in RLS)
create or replace function public.studio_device_likes_today(p_device_id text)
returns int
language sql
stable
as $$
  select count(*)::int
  from public.studio_likes
  where device_id = p_device_id
    and created_at >= (now() - interval '24 hours');
$$;

create or replace function public.studio_is_approved(p_submission_id bigint)
returns boolean
language sql
stable
as $$
  select exists(
    select 1
    from public.studio_submissions s
    where s.id = p_submission_id
      and s.status = 'approved'
  );
$$;

