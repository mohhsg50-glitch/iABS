  execute 'drop trigger if exists audit_integration_settings on public.integration_settings;';

  execute 'create trigger audit_announcements after insert or update or delete on public.announcements for each row execute function public.audit_trigger_fn();';
  execute 'create trigger audit_sponsors after insert or update or delete on public.sponsors for each row execute function public.audit_trigger_fn();';
  execute 'create trigger audit_highlight_clips after insert or update or delete on public.highlight_clips for each row execute function public.audit_trigger_fn();';
  execute 'create trigger audit_schedule after insert or update or delete on public.schedule for each row execute function public.audit_trigger_fn();';
  execute 'create trigger audit_faqs after insert or update or delete on public.faqs for each row execute function public.audit_trigger_fn();';
